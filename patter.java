import java.util.Scanner;
public class patter{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.println("Enter n value");
        int n = sc.nextInt();
        int i,j;
        for(i=0;i<=n;i++){
            for(j=0;i<=i;j++){
                System.out.println(j);
            }

        }
    }
}
